package com.company.service.dto.result;

import java.io.Serializable;
import java.math.BigInteger;

public class EmpInfoReport implements Serializable{
	private static final long serialVersionUID = 1L;
		private int empno;
		private String ename;
		private String dname;
		private double avgsal;
		private double sumsal;
		private BigInteger leaveCount;
		private BigInteger sumLeave;
		private BigInteger overtimeCount;
		private BigInteger overtimeSum;
		
		public EmpInfoReport() {
		}

		public EmpInfoReport(int empno, String ename, String dname, double avgsal, double sumsal, BigInteger leaveCount,
				BigInteger sumLeave, BigInteger overtimeCount, BigInteger overtimeSum) {
			super();
			this.empno = empno;
			this.ename = ename;
			this.dname = dname;
			this.avgsal = avgsal;
			this.sumsal = sumsal;
			this.leaveCount = leaveCount;
			this.sumLeave = sumLeave;
			this.overtimeCount = overtimeCount;
			this.overtimeSum = overtimeSum;
		}

		public int getEmpno() {
			return empno;
		}

		public void setEmpno(int empno) {
			this.empno = empno;
		}

		public String getEname() {
			return ename;
		}

		public void setEname(String ename) {
			this.ename = ename;
		}

		public String getDname() {
			return dname;
		}

		public void setDname(String dname) {
			this.dname = dname;
		}

		public double getAvgsal() {
			return avgsal;
		}

		public void setAvgsal(double avgsal) {
			this.avgsal = avgsal;
		}

		public double getSumsal() {
			return sumsal;
		}

		public void setSumsal(double sumsal) {
			this.sumsal = sumsal;
		}

		public BigInteger getLeaveCount() {
			return leaveCount;
		}

		public void setLeaveCount(BigInteger leaveCount) {
			this.leaveCount = leaveCount;
		}

		public BigInteger getSumLeave() {
			return sumLeave;
		}

		public void setSumLeave(BigInteger sumLeave) {
			this.sumLeave = sumLeave;
		}

		public BigInteger getOvertimeCount() {
			return overtimeCount;
		}

		public void setOvertimeCount(BigInteger overtimeCount) {
			this.overtimeCount = overtimeCount;
		}

		public BigInteger getOvertimeSum() {
			return overtimeSum;
		}

		public void setOvertimeSum(BigInteger overtimeSum) {
			this.overtimeSum = overtimeSum;
		}

		@Override
		public String toString() {
			return "EmpInfoReport [empno=" + empno + ", ename=" + ename + ", dname=" + dname + ", avgsal=" + avgsal
					+ ", sumsal=" + sumsal + ", leaveCount=" + leaveCount + ", sumLeave=" + sumLeave
					+ ", overtimeCount=" + overtimeCount + ", overtimeSum=" + overtimeSum + "]";
		}		
		
}
